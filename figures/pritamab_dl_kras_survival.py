# ── KM helpers ─────────────────────────────────────────────────────────────
def km(t, e, t_max=None):
    if len(t) == 0: return np.array([0, 1]), np.array([1.0, 1.0])
    if t_max is None: t_max = t.max()
    idx = np.argsort(t)
    ts, es = t[idx], e[idx]
    nar = len(ts); S = 1.0
    ks, ss = [0], [1.0]
    for ti, ei in zip(ts, es):
        if ei: S *= (1 - 1/max(nar, 1))
        nar -= 1
        ks.append(ti); ss.append(S)
    ks.append(max(t_max, ks[-1])); ss.append(ss[-1])
    return np.array(ks), np.array(ss)

def med_km(ts, ss):
    cross = np.where(np.array(ss) <= 0.5)[0]
    return ts[cross[0]] if len(cross) else np.nan

def logrank_p_simple(t1, e1, t2, e2):
    import math
    all_t = np.concatenate([t1, t2]); all_e = np.concatenate([e1, e2])
    ev_ts = np.unique(all_t[all_e == 1])
    O1=O2=E1=E2=0.0
    for ev in ev_ts:
        n1=np.sum(t1>=ev); n2=np.sum(t2>=ev); nt=n1+n2
        if nt==0: continue
        d1=np.sum((t1==ev)&(e1==1)); d2=np.sum((t2==ev)&(e2==1)); d=d1+d2
        O1+=d1; O2+=d2; E1+=d*n1/nt; E2+=d*n2/nt
    if E1==0 or E2==0: return 1.0
    chi2 = (O1-E1)**2/max(E1,1e-6) + (O2-E2)**2/max(E2,1e-6)
    return round(max(min(1 - math.exp(-0.5*chi2), 1.0), 0.001), 4)

