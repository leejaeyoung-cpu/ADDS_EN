    ax_os.step(ts_op, ss_op, where="post", color=clr,  lw=2.5, linestyle="solid")
    _tg2 = np.linspace(0, T_MAX, 300)
    _sop = np.interp(_tg2, ts_op, ss_op)
    _soc = np.interp(_tg2, ts_oc, ss_oc)
